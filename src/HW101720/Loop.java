package HW101720;

public class Loop {

    public static void main(String[] args) {
        for (int i = 1; i < 12; i++) {
            System.out.println("Player " + i);
        }

        for (int b = 1; b < 5; b++) {
            System.out.println("Team "+b);
        }

        for (int c = 3; c > 1; c--){
            System.out.println("Referee "+c);
        }
    }
}